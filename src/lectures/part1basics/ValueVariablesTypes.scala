package lectures.part1basics

object ValueVariablesTypes extends App{
  // Integer
  val x = 42
  println(x)

  // String
  val aString: String = "Hello"
  val anotherString: String = "goodbye"

  // Boolean
  val aBoolean: Boolean = false


  // Char
  val aChar: Char = 'a';

  // Short

  val aShort: Short = 1111

  // Long
  var aLong: Long = 2134123412342312421L;

  // Float
  var aFloat: Float = 2.0f;

  // Double
  var aDouble: Double = 6.0;

  // Variables
  var aVariable: Int = 4;
  aVariable = 7;




}
